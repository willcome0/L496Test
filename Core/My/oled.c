void OLED_DrawPoint(uint8_t x, uint8_t y, uint8_t mode)
{
    if (x>=OLED_X_MAX || y>=OLED_Y_MAX)
        return;
    uint8_t pos = y / 8;
    uint8_t bx = y % 8;
    uint8_t temp = 0x01 << bx;	// 低位在前
    if (mode)
        OLED_GRAM[x][pos] |= temp;	// 相应置1其余不变，为1的那位点亮
    else
        OLED_GRAM[x][pos] &= ~temp;	// 相应置0其余不变
}

void OLED_Fill(uint8_t x1, uint8_t y1, uint8_t x2, uint8_t y2, uint8_t mode)
{
    for (uint8_t x=x1; x<=x2; x++)
        for (uint8_t y=y1; y<=y2; y++)
            OLED_DrawPoint(x, y, mode);
}

void OLED_Show_Char(uint8_t x, uint8_t y, char chr, uint8_t size, uint8_t mode)
{
    uint8_t temp;
    uint8_t y0 = y;
    uint8_t csize = (size/8+((size%8)?1:0))*(size/2);		//得到字体一个字符对应点阵集所占的字节数
    chr = chr-' ';//得到偏移后的值	，空格在ASCII中十进制为32，而在字库中是第一位
    for (uint8_t t=0; t<csize; t++)
    {
        switch (size) //选择调用的字库
        {
            case 12:
                temp = asc2_1206[chr][t];
                break;
            case 16:
                temp = asc2_1608[chr][t];
                break;
            case 24:
                temp = asc2_2412[chr][t];
                break;
            default:
                break;
        }
        for (uint8_t t1=0; t1<8; t1++)
        {
            if (temp&0x80)	// 重高位开始写
                OLED_DrawPoint(x, y, mode);
            else
                OLED_DrawPoint(x, y, !mode);
            temp <<= 1;
            y++;
            if ((y-y0) == size)
            {
                y = y0;
                x++;
                break;
            }
        }
    }
}

void OLED_Show_Str(uint8_t x, uint8_t y, char *ch, uint8_t size, uint8_t mode)
{
    while ((*ch<='~')&&(*ch>=' ')) //判断是不是非法字符
    {
        OLED_Show_Char(x, y, *ch, size, mode);
        x += size/2;
        ch++;
    }
}







